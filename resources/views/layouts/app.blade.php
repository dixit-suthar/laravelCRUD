        $(function() {
            var dateFormat = "mm/dd/yy",
                from = $("#from")
                .datepicker({
                    minDate: 0,
                    maxDate: +20,
                    defaultDate: "+1d",
                    changeMonth: true,
                    numberOfMonths: 1
                })
                .on("change", function() {
                    to.datepicker("option", "minDate", getDate(this));
                }),
                to = $("#to").datepicker({
                    minDate: 0,
                    maxDate: +20,
                    defaultDate: "+1d",
                    changeMonth: true,
                    numberOfMonths: 1
                })
                .on("change", function() {
                    from.datepicker("option", "maxDate", getDate(this));
                });

            function getDate(element) {
                var date;
                try {
                    date = $.datepicker.parseDate(dateFormat, element.value);
                } catch (error) {
                    date = null;
                }

                return date;
            }

            function datediff(first, second) {
                return Math.round((second - first) / (1000 * 60 * 60 * 24)) + 1;
            }

            function parseDate(str) {
                var mdy = str.split('/');
                return new Date(mdy[2], mdy[0] - 1, mdy[1]);
            }
            $('#to').change(function() {
                if ($('#from, #to').val() != "" && $('#to').val() != "") {
                    $('#numDays').val(datediff(parseDate($('#from').val()), parseDate($('#to').val())))
                }
            })
        });
